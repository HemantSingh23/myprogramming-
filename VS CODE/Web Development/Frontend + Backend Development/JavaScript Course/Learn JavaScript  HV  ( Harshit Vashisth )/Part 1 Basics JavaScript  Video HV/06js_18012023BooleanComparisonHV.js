//  //    ==   and   ====        -->
//
// == check only value then return true or false
// === check only value and datatype then return true or false
//

let num3 = "7";
let num4 =  7 ;
console.log(num3==num4);// true // value is same
console.log(num3===num4);// false // value is same datatype is different




//  //    !=  and  !==     -->
//
// != print true if different value
// !== print true if different value and datatype

console.log(num3!=num4);// false // value is same
console.log(num3!==num4);// true // value is not same
// and datatype is also not same









