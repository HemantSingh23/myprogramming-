
///////////////////////             REPEAT 8 USING WHILE LOOP              /////////////////////// 

/*  Write a program to Repeat 8 using while Loop.  */


#include<stdio.h>
int main()
{
    int a;

    printf("Enter the number\n");
    scanf("%d",&a);

    while(a==8)
    {
        printf("%d",a);
    }


    return 0;
}
