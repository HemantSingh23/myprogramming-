

/////////////////////////////////               WHAT IS DYNAMIC MEMORY ALLOCATION??                  ////////////////////////////////////////////// 

/* 

C IS A LANGUAGE WITH SOME FIXED RULES OF PROGRAMMING. FOR EXAMPLE: CHANGING THE SIZE OF AN ARRAY IS NOT ALLOWED DURING RUNTIME.



Dynamic Memory Allocation ::

Dynamic memory allocation is a way to allocate memory to a data structure during the runtime we can use DMA function 
available in C to allocate and free memory during runtime.



Function for DMA in C ::

Following functions are available in C to perform dynamic memory allocation:

malloc()
calloc()
free()
realloc()




*/